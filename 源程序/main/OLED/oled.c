/*******************
数组各位++
参数: 数组指针
*******************/
static void Add(uint8_t *num)
{
	uint8_t i;
	for(i = 0;i<8;i++)
	{
		(*(num+i))--;
		if((*(num+i)<2))
		{
			*(num+i) = 6;
		}
	}
}

